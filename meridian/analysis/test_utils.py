  channel_names = (
      [f"ch_{i}" for i in range(3)]
      + [f"rf_ch_{i}" for i in range(2)]
      + [f"organic_ch_{i}" for i in range(2)]
      + [f"organic_rf_ch_{i}" for i in range(1)]
  )
